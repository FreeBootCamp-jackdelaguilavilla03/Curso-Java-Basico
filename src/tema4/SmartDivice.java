package tema4;

public class SmartDivice {
    String color;
    String fabricante;
    float Pantalla;
    String modelo;
    String sistemaOperativo;
    String Marca;

    public SmartDivice() {
    }

    public SmartDivice(String color, String fabricante, float Pantalla, String modelo, String sistemaOperativo, String Marca) {
        this.color = color;
        this.fabricante = fabricante;
        this.Pantalla = Pantalla;
        this.modelo = modelo;
        this.sistemaOperativo = sistemaOperativo;
        this.Marca = Marca;
    }
}
